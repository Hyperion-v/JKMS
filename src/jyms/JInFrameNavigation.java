        setHtmlText(jButtonAlarmOutCtrl, Locales.getString(      "ClassStrings", "JInFrameNavigation.jButtonAlarmOutCtrl"));//"报警输出控制"
        setHtmlText(jButtonDVRCheckTime, Locales.getString(      "ClassStrings", "JInFrameNavigation.jButtonDVRCheckTime"));//"设备校时"
        setHtmlText(jButtonDVRManage, Locales.getString(         "ClassStrings", "JInFrameNavigation.jButtonDVRManage"));//"设备管理"
        setHtmlText(jButtonDVRMaint, Locales.getString(          "ClassStrings", "JInFrameNavigation.jButtonDVRMaint"));//"设备维护"
        setHtmlText(jButtonRecordSchedule, Locales.getString(    "ClassStrings", "JInFrameNavigation.jButtonRecordSchedule"));//"录像存储计划"
        setHtmlText(jButtonUserManage, Locales.getString(        "ClassStrings", "JInFrameNavigation.jButtonUserManage"));//"用户管理"
        setHtmlText(jButtonSysParasConfig, Locales.getString(    "ClassStrings", "JInFrameNavigation.jButtonSysParasConfig"));//"系统参数配置"
        setHtmlText(jButtonLogMaint, Locales.getString(          "ClassStrings", "JInFrameNavigation.jButtonLogMaint"));//"日志管理"
        setHtmlText(jButtonDVRGroup, Locales.getString(          "ClassStrings", "JInFrameNavigation.jButtonDVRGroup"));//"设备分组管理"
        setHtmlText(jButtonRecordQuicklySetup, Locales.getString("ClassStrings", "JInFrameNavigation.jButtonRecordQuicklySetup"));//"录像快速设置"
        jLabelOperationControl.setText( Locales.getString("ClassStrings", "JInFrameNavigation.jLabelOperationControl"));//"操作与控制"
