    private void intialParas(){
        jLabelDVRType.setVisible(false);
        jPanelDVRType.setVisible(false);
//        ListDVRType = CodesBean.getCodesList(CommonParas.DVRType.DVRTYPE,"1",sFileName);
//        String[] DVRTypes = new String[ListDVRType.size()];
//        
//        for (int i=0;i<ListDVRType.size();i++){
//            DVRTypes[i] = ListDVRType.get(i).getCodename();
//        }
//        comboboxDVRType = new MulitCombobox(DVRTypes,DVRTypes,LengthOfCombobox);
//        
//        jPanelDVRType.setLayout(new GridLayout(1, 1));
//        jPanelDVRType.add(comboboxDVRType);
//        jPanelDVRType.updateUI();
//        jPanelDVRType.setBorder(null);
        
        //设备
        ArrayList<String> listDVRs = new ArrayList();
        ArrayList<String> listSerialNOs = new ArrayList();
        
        for (int i=0;i<CommonParas.g_listDeviceDetailPara.size();i++){
            DeviceParaBean deviceParaBean =  CommonParas.getDeviceParaBean(i, sFileName);
            if (deviceParaBean != null) {
                listDVRs.add(deviceParaBean.getAnothername());
                listSerialNOs.add(deviceParaBean.getSerialNO());
            }
        }
        Object[] DVRs = new String[listDVRs.size()];
        Object[] DVRSerialNOs = new String[listSerialNOs.size()];
        
        if (listDVRs.size() > 0) DVRs = listDVRs.toArray(new Object[listDVRs.size()]);
        if (listSerialNOs.size() > 0) DVRSerialNOs = listSerialNOs.toArray(new Object[listSerialNOs.size()]);
        
        comboboxDVR = new MulitCombobox(DVRs, DVRSerialNOs, DVRs, LengthOfCombobox);
        initialComboboxPanel( jPanelDVR , comboboxDVR);

        
        //现在只针对编码设备进行设置
        //ListResourceType = CodesBean.getCodesList(CommonParas.DVRType.DVRTYPE_ENCODINGDVR_CODE,"1",sFileName);
        ListResourceType = CodesBean.getCodesList(CommonParas.DVRResourceType.RESTYPE_ENCODINGDVR_CODE,"1",sFileName);
        String[] DVRResources = new String[ListResourceType.size()];
        String[] DVRResourceCodes = new String[ListResourceType.size()];
        for (int i=0;i<ListResourceType.size();i++){
            DVRResources[i] = ListResourceType.get(i).getCodename();
            DVRResourceCodes[i] = ListResourceType.get(i).getCode();
        }
        comboboxResourceType = new MulitCombobox(DVRResources,DVRResourceCodes,DVRResources,LengthOfCombobox);
        initialComboboxPanel( jPanelResourceType , comboboxResourceType);

        
        //操作用户
        listUserNmes = UsersBean.getUserNameList(sFileName);
        String[] UserNmes = new String[listUserNmes.size()];
        
        for (int i=0;i<listUserNmes.size();i++){
            UserNmes[i] = listUserNmes.get(i);
        }
        comboboxUserNames = new MulitCombobox(UserNmes,UserNmes,UserNmes,LengthOfCombobox);
        initialComboboxPanel( jPanelUserNmes , comboboxUserNames);

        
        //日志类型LogType.LOG_TYPE
        ListLogType = CodesBean.getCodesList(CommonParas.LogType.LOG_TYPE_CODE,"1",sFileName);
        String[] LogTypes = new String[ListLogType.size()];
        String[] LogTypeCodes = new String[ListLogType.size()];
        
        for (int i=0;i<ListLogType.size();i++){
            LogTypes[i] = ListLogType.get(i).getCodename();
            LogTypeCodes[i] = ListLogType.get(i).getCode();
        }
        comboboxLogType = new MulitCombobox(LogTypes,LogTypeCodes,LogTypes,LengthOfCombobox);
        initialComboboxPanel( jPanelLogType , comboboxLogType);

        //设置自定义UI
        CommonParas.setJButtonUnDecorated(jButtonExit);
        //返回列表初始化
//        for (int i=0;i<3;i++){
//            listReturn.add("");
//        }
    }
