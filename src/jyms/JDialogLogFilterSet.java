    private void jButtonSetupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSetupActionPerformed
        // TODO add your handling code here:
        if (!comboboxUserNames.ifCheckAll()){
            listReturn.add(ClientLogBean.getInLimitOfUserName(comboboxUserNames.getCheckedCodes(), sFileName));//" CLIENTLOG.UserName in ("+ comboboxUserNames.getCheckedCodes() + ")");
            listReturnText.add(comboboxUserNames.getCheckedValues());
        }
        
        if (!comboboxLogType.ifCheckAll()){
            listReturn.add(ClientLogBean.getInLimitOfLogType(comboboxLogType.getCheckedCodes(), sFileName));//" CLIENTLOG.LogType in ("+ comboboxLogType.getCheckedCodes() + ")");
            listReturnText.add(comboboxLogType.getCheckedValues());
        }
        if (!comboboxDVR.ifCheckAll()){
            listReturn.add(ClientLogBean.getInLimitOfSerialNO(comboboxDVR.getCheckedCodes(), sFileName)); //" CLIENTLOG.SERIALNO in ("+comboboxDVR.getCheckedCodes() + ")");
            listReturnText.add(comboboxDVR.getCheckedValues());
        }
        if (!comboboxResourceType.ifCheckAll()){
            listReturn.add(ClientLogBean.getInLimitOfObjectType(comboboxResourceType.getCheckedCodes(), sFileName));//" CLIENTLOG.ObjectType in ("+comboboxResourceType.getCheckedCodes() + ")");
            listReturnText.add(comboboxResourceType.getCheckedValues());
        }
        
        
        iState = 1;//设置
        this.dispose();
    }//GEN-LAST:event_jButtonSetupActionPerformed

