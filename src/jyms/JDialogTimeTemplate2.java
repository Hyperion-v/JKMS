        strSchedTimeWeek[iWeek-1].struAlarmTime[0].byStartHour = byStartHour;
        strSchedTimeWeek[iWeek-1].struAlarmTime[0].byStartMin = byStartMin;
        strSchedTimeWeek[iWeek-1].struAlarmTime[0].byStopHour = byStopHour;
